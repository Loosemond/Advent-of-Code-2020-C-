def countTrees(input , stepRight , steps_Down):
    input = input.split()
    linha = 0
    caracter = 0
    caracterSize = len(input[0])
    trees = 0
    lineSize = len(input)
    while (linha < lineSize - steps_Down):
        

        caracter = stepRight + caracter
        print("linha: {}   caracter: {}".format(linha,caracter))
        if (input[linha + steps_Down][caracter] == '#'):
            trees = trees + 1
            
        if (caracter >= caracterSize  - stepRight ):
            caracter = caracter - caracterSize

            
        
        linha = linha + steps_Down
        
    return trees





